from __future__ import annotations

import argparse
import json
from pathlib import Path


def percentile(values: list[float], q: float) -> float:
    if not values:
        return 0.0
    if q <= 0:
        return float(min(values))
    if q >= 100:
        return float(max(values))
    values_sorted = sorted(values)
    idx = (len(values_sorted) - 1) * q / 100.0
    lo = int(idx)
    hi = min(lo + 1, len(values_sorted) - 1)
    if lo == hi:
        return float(values_sorted[lo])
    w = idx - lo
    return float(values_sorted[lo] * (1.0 - w) + values_sorted[hi] * w)


def main() -> None:
    parser = argparse.ArgumentParser(description="Evaluate replay jsonl generated by recorder")
    parser.add_argument("--path", required=True, help="Path to replay jsonl file")
    parser.add_argument("--json", action="store_true", help="Print summary as JSON")
    args = parser.parse_args()

    path = Path(args.path)
    if not path.exists():
        raise SystemExit(f"File not found: {path}")

    frame_count = 0
    first_ts = None
    last_ts = None
    total_entities = 0

    process_ms_values: list[float] = []
    detect_ms_values: list[float] = []
    track_ms_values: list[float] = []
    post_ms_values: list[float] = []

    seen_ids: set[int] = set()
    id_births = 0
    max_drop = 0

    for line in path.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line:
            continue

        payload = json.loads(line)
        frame_count += 1

        ts = int(payload.get("frame_ts_ms", 0))
        if first_ts is None:
            first_ts = ts
        last_ts = ts

        entities = payload.get("entities", [])
        total_entities += len(entities)

        for entity in entities:
            tid = int(entity.get("id", -1))
            if tid not in seen_ids:
                if tid >= 0:
                    id_births += 1
                seen_ids.add(tid)

        meta = payload.get("meta", {})
        if "process_ms" in meta:
            process_ms_values.append(float(meta["process_ms"]))
        if "detect_ms" in meta:
            detect_ms_values.append(float(meta["detect_ms"]))
        if "track_ms" in meta:
            track_ms_values.append(float(meta["track_ms"]))
        if "post_ms" in meta:
            post_ms_values.append(float(meta["post_ms"]))
        if "decode_dropped" in meta:
            max_drop = max(max_drop, int(meta["decode_dropped"]))

    duration_s = 0.0
    if first_ts is not None and last_ts is not None and last_ts > first_ts:
        duration_s = (last_ts - first_ts) / 1000.0

    summary = {
        "frames": frame_count,
        "duration_s": round(duration_s, 3),
        "effective_fps": round(frame_count / duration_s, 3) if duration_s > 0 else 0.0,
        "avg_entities_per_frame": round(total_entities / frame_count, 3) if frame_count > 0 else 0.0,
        "unique_ids": len([x for x in seen_ids if x >= 0]),
        "id_births": id_births,
        "process_ms_avg": round(sum(process_ms_values) / len(process_ms_values), 3) if process_ms_values else 0.0,
        "process_ms_p95": round(percentile(process_ms_values, 95), 3) if process_ms_values else 0.0,
        "detect_ms_avg": round(sum(detect_ms_values) / len(detect_ms_values), 3) if detect_ms_values else 0.0,
        "track_ms_avg": round(sum(track_ms_values) / len(track_ms_values), 3) if track_ms_values else 0.0,
        "post_ms_avg": round(sum(post_ms_values) / len(post_ms_values), 3) if post_ms_values else 0.0,
        "decode_dropped_max": max_drop,
    }

    if args.json:
        print(json.dumps(summary, ensure_ascii=False, indent=2))
        return

    print("Replay Summary")
    print(f"- file: {path}")
    print(f"- frames: {summary['frames']}")
    print(f"- duration_s: {summary['duration_s']}")
    print(f"- effective_fps: {summary['effective_fps']}")
    print(f"- avg_entities_per_frame: {summary['avg_entities_per_frame']}")
    print(f"- unique_ids: {summary['unique_ids']} (births={summary['id_births']})")
    print(f"- process_ms avg/p95: {summary['process_ms_avg']} / {summary['process_ms_p95']}")
    print(f"- detect_ms_avg: {summary['detect_ms_avg']}")
    print(f"- track_ms_avg: {summary['track_ms_avg']}")
    print(f"- post_ms_avg: {summary['post_ms_avg']}")
    print(f"- decode_dropped_max: {summary['decode_dropped_max']}")


if __name__ == "__main__":
    main()
